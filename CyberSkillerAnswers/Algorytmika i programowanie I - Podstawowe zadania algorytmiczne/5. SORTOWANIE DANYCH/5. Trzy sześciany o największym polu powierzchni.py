n = int(input())

dimensions = []

def calculate_surface_area(a, b, h):
    return 2 * (a * b + b * h + a * h)

for i in range(n):
    input_data = list(map(int, input().split()))

    a = input_data[0]
    b = input_data[1]
    h = input_data[2]

    area = calculate_surface_area(a, b, h)

    dimensions.append((a, b, h, area))

sorted_dimensions = sorted(dimensions, key=lambda x: x[3], reverse=True)

top_three = sorted(sorted_dimensions[:3], key=lambda x: x[3], reverse=True)

for dim in top_three:
    print(dim[0], dim[1], dim[2], dim[3])